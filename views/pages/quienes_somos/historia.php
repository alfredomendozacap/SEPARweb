                <div class="capitulo">
                    <p class="titCapitulo">VII. Replanear el horizonte y remontar (2010-2013)</p>
                    <div class="contenido">
                        <div class="parrafo">
                            <img class="imgPrin" src="assets/view/img/quienes_somos/historia/pictures/cap8.PNG" alt="" width="260">
                            <p>Mientras una crisis económica global recorre el mundo desde 2009, el Perú —como pocos
                                países— ha mantenido sus indicadores de crecimiento y su ubicación de nación de renta
                                media creciente. Sin embargo, las brechas sociales históricas, como las reparaciones
                                postviolencia, son aún una deuda pendiente en la política nacional. En este marco, los
                                programas sociales del Estado y los de Responsabilidad Social Corporativa son
