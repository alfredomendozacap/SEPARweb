                <div class="capitulo">
                    <p class="titCapitulo">I. Historia y vicisitudes</p>
                    <div class="contenido">
                        <div class="parrafo">
                            <img class="imgPrin" src="assets/view/img/quienes_somos/historia/pictures/cap1.PNG" alt="" width="260">
                            <p>Uno de los valles interandinos más amplios y fértiles del Perú se encuentra entre los
                                3,200 y 3,580 m.s.n.m. Es el valle del Mantaro, cuyos suelos son sumamente productivos
                                para la agricultura y ganadería diversificada. En él se desarrollan actividades
                                agroindustriales de subsistencia, como de manufactura, artesanía y arte popular,
                                expresados en los mates burilados de Cochas, la platería de San Jerónimo o la
                                textilería de Hualhuas. Las danzas costumbristas y bandas musicales son parte de una
                                identidad regional influyente. Los orígenes míticos del valle hablan del enfrentamiento
                                de los dioses tutelares de Pariakaka y Wallallo Carhuancho por los dominios de
                                Huarochirí y el valle del Mantaro, respectivamente.</p>

                            <p>La población es heredera del señorío regional preinca de los Hanan Wankas, Hurin
                                Wankas
                                y Jatun Xauxas del siglo XIV, como son la Confederación Chanka de los Anccaras,
                                Chocorbos y Tayaccasas, etc., para el caso de Huancavelica y Apurímac. Durante la
                                Colonia los wankas, gracias a una “estrategia” de alianza con los conquistadores para
                                enfrentar a los incas, mantuvieron dominio territorial, así como una relativa
                                prosperidad y desarrollo ulterior de una identidad mestiza. La alianza wanka-española
                                solo terminó con la independencia definitiva del Perú y la derrota de Canterac en las
                                Batallas de Junín y Ayacucho, en el primer tercio del siglo XIX.
                                En el último tercio del mismo siglo XIX —ya en la Etapa Republicana—, durante la Guerra
                                del Pacífico entre Perú y Chile, la prolongación de la guerra ocasionaba «perjuicios»
                                económicos a una naciente burguesía «emprendedora» wanka, que mantenía una fuerte
                                posesión de tierras y actividad mercantil dinámica. Asumieron una conducta
                                «colaboracionista» con el ejército chileno a fin de que las acciones de guerra
                                concluyeran cuanto antes por lo oneroso que resultaba la frecuente demanda de
                                avituallamiento del ejército chileno.</p>
                            <img class="imgSecun" src="assets/view/img/quienes_somos/historia/pictures/cap1-1.PNG" alt="" width="250">
                            <p>Durante el primer tercio del siglo XX la actividad minera había crecido de tal
                                manera
                                que
                                impulsó un mayor desarrollo de vías de acceso y, sobre todo, la ejecución del
                                proyecto
                                ferroviario más emblemático del siglo en los Andes centrales: el Ferrocarril
                                Central,
                                ¡el
                                más alto del mundo! Este hecho marcó el establecimiento de una economía de enclaves
                                mineros
                                y de diversificación de la actividad comercial. Pero, también, produjo una profunda
                                diferenciación campesina o exclusión rural de grandes zonas de Huancavelica, Pasco
                                y
                                Junín,
                                cuyas ciudades se convirtieron en focos de atracción de pobladores empobrecidos.
                                La Reforma Agraria de los sesenta, que trató de redefinir la tenencia de la tierra
                                y
                                ganado
                                con un enfoque empresarial —Sociedad Agraria de Interés Social, SAIS—, fue
                                interrumpida
                                en
                                los setenta y «desestructurada» en los ochenta por acción de la violencia y el
                                terror
                                desatados en esta década, que provocó el desplazamiento forzado de comunidades
                                enteras
                                a
                                las ciudades de Junín, Huancavelica, Ayacucho y principalmente Lima.</p>
                            <p>Estas distintas etapas de la historia han perfilado la identidad y la cultura de la
                                región. Así, el Huaylash evoca la relación telúrica del wanka precolonial; la
                                Chonguinada parodia un baile señorial colonial; la Maqtada evoca a la soldadesca
                                india
                                en la guerra con Chile; la fuerza del huayno wanka, las bandas musicales y las
                                «fusiones» musicales siguen siendo una expresión de identidad regional, llevada a
                                niveles de industria del espectáculo.
                            </p>

